
public class RangosPuertos {
	
	int idNombre;
	String rangoPuertos;	
	
	public RangosPuertos(int idNombre, String rangoPuertos) {
		super();
		this.idNombre = idNombre;
		this.rangoPuertos = rangoPuertos;
	}
	public int getIdNombre() {
		return idNombre;
	}
	public void setIdNombre(int idNombre) {
		this.idNombre = idNombre;
	}
	public String getRangoPuertos() {
		return rangoPuertos;
	}
	public void setRangoPuertos(String rangoPuertos) {
		this.rangoPuertos = rangoPuertos;
	}
	
}
